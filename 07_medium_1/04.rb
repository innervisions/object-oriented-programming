# 04 - Circular Queue
class CircularQueue
  def enqueue(object)
    array.shift if array.size == size
    array << object
  end

  def dequeue
    array.shift
  end

  private

  attr_reader :array, :size

  def initialize(size)
    @size = size
    @array = []
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue.nil?

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
puts queue.dequeue == 7
puts queue.dequeue.nil?

